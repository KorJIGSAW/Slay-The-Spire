void DrawBox(int width, int height) {
	int i, j;

	// Top border
	GotoXY(1, 1);
	printf("┌");
	for (i = 3; i < width - 1; i+=2) {
		printf("─");
	}
	printf("┐");

	// Middle rows
	for (i = 2; i < height - 1; i++) {
		GotoXY(1, i);
		printf("│");
		GotoXY(width-1, i);
		printf("│");
	}

	// Bottom border
	GotoXY(1, height-1);
	printf("└");
	for (i = 3; i < width - 1; i+=2) {
		printf("─");
	}
	printf("┘");
}

